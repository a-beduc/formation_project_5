def sum(a, b):
    """
    Return the sum of two numbers.
            Parameters:
                    a : A number
                    b : A number
            Returns:
                    a + b
    """
    return a + b


def subtraction(a, b):
    """
    Return the subtraction of two numbers.
        Parameters:
                a (A number)
                b (A number)
        Returns:
                number : a - b
    """
    return a - b
